    /**
     * 把java pojo对象存到 redis hash结构中
     *
     * @param key
     * @param pojo pojo对象
     * @return
     */
    public static <T> boolean hSetPojo(String key, T pojo) {
        //pojo转Map
        Map<String, String> describe = null;
        try {
            describe = BeanUtils.describe(pojo);
            redisTemplate.opsForHash().putAll(key, describe);
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
        return true;
    }

    /**
     * 从redis hash 中取出 转成为pojo
     * Map转pojo
     *
     * @param key
     * @param cal 类的类型
     * @return
     */
    public static <U> U hGetPojo(String key, Class<U> cal) {
        try {
            U temp = cal.newInstance();
            Map map = redisTemplate.opsForHash().entries(key);
            ConvertUtils.register(new Converter() {
                @Override
                public <T> T convert(Class<T> type, Object value) {
                    SimpleDateFormat sfStart = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", java.util.Locale.ENGLISH);
                    try {
                        return (T) sfStart.parse(value.toString());
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                    return null;
                }
            }, Date.class);
            BeanUtils.populate(temp, map);
            return temp;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

