    /**
     * 普通缓存放入并设置时间
     *
     * @param key   键
     * @param value 值
     * @param time  时间(秒) time要大于0 如果time小于等于0 将设置无限期
     * @return true成功 false 失败
     */
    public boolean set(String key, Object value, long time) {
        try {
            if (time > 0) {
                redisTemplate.opsForValue().set(key, value, time, TimeUnit.SECONDS);
            } else {
                set(key, value);
            }
            return true;
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

    /**
     * 分布式锁的简单实现
     * 悲观锁
     *
     * @param key   key有就是有锁
     * @param value uuid 解锁的时候要lua判断不能误解
     * @param time  秒
     */
    public static boolean setIfAbsent(String key, Object value, long time) {
        try {
            return redisTemplate.opsForValue().setIfAbsent(key, value, time, TimeUnit.SECONDS);
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

    /**
     * 获得锁的重试机制
     *
     * @param key
     * @param value
     * @param timeOut   持续存活的时间 秒
     * @param retry     重试次数
     * @param sleepTime 获取失败后休眠时间
     */
    public Boolean lockRetry(String key, String value, Long timeOut, Integer retry, Long sleepTime) {
        Boolean flag = false;
        try {
            for (int i = 0; i < retry; i++) {
                flag = setIfAbsent(key, value, timeOut);
                if (flag) {
                    break;
                }
                Thread.sleep(sleepTime);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return flag;
    }

    /**
     * 分布式锁--解锁
     * 悲观锁
     *
     * @param key   锁标识
     * @param value uuid 防止误解锁
     */
    public static boolean outLock(String key, String value) {
        String text = "if redis.call(\"get\",KEYS[1]) == ARGV[1] then return redis.call(\"del\",KEYS[1]) else return 0 end";
        DefaultRedisScript<Long> script = new DefaultRedisScript();
        script.setScriptText(text);
        script.setResultType(Long.class);
        //redistempalte封装的execute来实现调用，其中第二个参数为lua脚本中的KEYS[1],后面为可变参数，可变参数即传入我们需要的值，分别对应ARGV[1],ARGV[2]
        Long execute = redisTemplate.execute(script, Arrays.asList(key), value);
        if (execute > 0) {
            return true;
        }
        return false;
    }

    /**
     * redis乐观锁 应用 秒杀 库存扣减
     */
    private void modifyAmount(int count) {
        redisTemplate.watch("kucun");
        String val = String.valueOf(redisTemplate.opsForValue().get("kucun"));
        int valInt = Integer.valueOf(val);
        if (valInt > 0 && valInt - count > 0) {
            redisTemplate.multi();
            redisTemplate.opsForValue().increment("kucun", -count);
            List<Object> list = redisTemplate.exec();
            if (list == null || list.size() == 0) {
                System.out.println("对不起,抢购失败,请重试:" + list.size());
            } else {
                System.out.println("抢购成功");
            }
        }
    }

    /**
     * 递增
     *
     * @param key   键
     * @param delta 要增加几(大于0)
     * @return
     */
    public long incr(String key, long delta) {
        if (delta < 0) {
            throw new RuntimeException("递增因子必须大于0");
        }
        return redisTemplate.opsForValue().increment(key, delta);
    }

    /**
     * 递减
     *
     * @param key   键
     * @param delta 要减少几(小于0)
     * @return
     */
    public long decr(String key, long delta) {
        if (delta < 0) {
            throw new RuntimeException("递减因子必须大于0");
        }
        return redisTemplate.opsForValue().increment(key, -delta);
    }

